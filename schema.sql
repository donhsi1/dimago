-- DimaGo dict_<TR>_<NA>.db schema
-- Generated from dict_TH_CN.db

CREATE TABLE category (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    language_tag  TEXT,                /* Country Code:user native language on the device */
    lang_translate  TEXT,              /* Country Code: language to learn */
    lang_native   TEXT,                /* Country Code:native language */
    name_native   TEXT NOT NULL,
    name_translate TEXT NOT NULL,
    name_EN       TEXT NOT NULL,
    lesson_id     INTEGER DEFAULT 0,   /* unique Id different from id index */
    user_id       INTEGER DEFAULT 0,
    access        INTEGER DEFAULT 0,   /* access code public vs private */
    difficulty    INTEGER DEFAULT 0,   /* 0-easy, 1-medium, 2-hard */
    date_created  TEXT,
    date_modified TEXT,
    count         INTEGER DEFAULT 0,   /* count of words in this category, can be updated when add/remove word */
    count_down    INTEGER DEFAULT 5,   /* count down counter seconds for challenge mode */
    practice_type INTEGER DEFAULT 0,   /* three types of practices/challenges; 0:word, 1:phrase, 2:photo */
    challenge     INTEGER DEFAULT 0,   /* keep the challenge result(need definition) */
    photo         TEXT,                /* link to thumbnail image */
    is_favorite   INTEGER DEFAULT 0，
    score1   INTEGER DEFAULT 0,
    score2   INTEGER DEFAULT 0,
    score3   INTEGER DEFAULT 0,
    score4   INTEGER DEFAULT 0,
    score5   INTEGER DEFAULT 0,
    score6   INTEGER DEFAULT 0,
    score7   INTEGER DEFAULT 0,
    score8   INTEGER DEFAULT 0
);

CREATE TABLE word (
    id                       INTEGER PRIMARY KEY,
    name_native              TEXT NOT NULL,
    name_translate           TEXT NOT NULL,
    name_EN                  TEXT NOT NULL,
    roman_native             TEXT,
    roman_translate          TEXT,
    audio_translate          BLOB,  /* load on demand, pre-load for frequently used word */
    audio_native             BLOB,
    definition_native        TEXT,
    action_native            TEXT,
    definition_translate     TEXT,
    action_translate         TEXT,
    sample1_native           TEXT,
    sample1_translate        TEXT,
    sample1_translate_roman  TEXT, /* romanized translate sample (e.g. Thai RTGS) */
    sample1_native_audio     BLOB,
    sample1_translate_audio  BLOB,
    category_index           INTEGER,
    photo                    TEXT,  /* link to thumbnail image */
    user_id                  INTEGER DEFAULT 0,
    date_created             TEXT DEFAULT '2026-03-30',
    date_modified            TEXT,
    use_count                INTEGER DEFAULT 0,
    is_favorite              INTEGER DEFAULT 0
);

CREATE INDEX idx_word_category ON word(category_index);
CREATE INDEX idx_word_favorite ON word(is_favorite);


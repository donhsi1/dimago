    ON vocabulary_bundles FOR SELECT TO anon, authenticated USING (true);

DROP POLICY IF EXISTS "lessons_select_public" ON lessons;
CREATE POLICY "lessons_select_public"
    ON lessons FOR SELECT TO anon, authenticated USING (true);

DROP POLICY IF EXISTS "words_select_public" ON words;
CREATE POLICY "words_select_public"
    ON words FOR SELECT TO anon, authenticated USING (true);

DROP POLICY IF EXISTS "word_photo_blobs_select_public" ON word_photo_blobs;
CREATE POLICY "word_photo_blobs_select_public"
    ON word_photo_blobs FOR SELECT TO anon, authenticated USING (true);

